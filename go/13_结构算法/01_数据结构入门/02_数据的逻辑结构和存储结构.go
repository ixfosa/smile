// 存储结构（物理结构）
	// 数据的存储结构，也就是物理结构，指的是数据在物理存储空间上选择集中存放还是分散存放。
	// 假设要存储大小为 10G 的数据，则集中存放就如图 3a) 所示，分散存放就如图 3b）所示。
